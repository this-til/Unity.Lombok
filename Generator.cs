            MethodDeclarationSyntax writeField = MethodDeclaration(
                    IdentifierName(
                        "void"
                    ),
                    "write"
                )
                .AddModifiers(
                    Token(
                        SyntaxKind.PublicKeyword
                    ),
                    Token(
                        SyntaxKind.StaticKeyword
                    )
                )
                .AddParameterListParameters(
                    Parameter(
                            Identifier(
                                "writer"
