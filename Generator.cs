                );

                writeDeltaFieldStatement.Add(
                    ExpressionStatement(
                        InvocationExpression(
                            IdentifierName(
                                $"Unity.Netcode.NetworkVariableSerialization<{type}>.WriteDelta"
                            ),
                            ArgumentList(
                                SeparatedList(
                                    new List<ArgumentSyntax>() {
                                        Argument(
                                            IdentifierName(
                                                "writer"
                                            )
                                        ),
                                        Argument(
                                                MemberAccessExpression(
                                                    SyntaxKind.SimpleMemberAccessExpression,
                                                    IdentifierName(
                                                        "value"
                                                    ),
                                                    IdentifierName(
                                                        cSharpSyntaxNode.ToString()
                                                    )
                                                )
                                            )
                                            .WithRefKindKeyword(
                                                Token(
                                                    SyntaxKind.RefKeyword
                                                )
                                            ),
                                        Argument(
                                                MemberAccessExpression(
                                                    SyntaxKind.SimpleMemberAccessExpression,
                                                    IdentifierName(
                                                        "previousValue"
                                                    ),
                                                    IdentifierName(
                                                        cSharpSyntaxNode.ToString()
                                                    )
                                                )
                                            )
                                            .WithRefKindKeyword(
                                                Token(
                                                    SyntaxKind.RefKeyword
                                                )
                                            )
                                    }
                                )
                            )
                        )
                    )
                );

                duplicateValueFieldStatement.Add(
                    IfStatement(
                        PrefixUnaryExpression(
                            SyntaxKind.LogicalNotExpression,
                            InvocationExpression(
                                IdentifierName(
                                    "object.Equals"
                                ),
                                ArgumentList(
                                    SeparatedList(
                                        new[] {
                                            Argument(
                                                MemberAccessExpression(
                                                    SyntaxKind.SimpleMemberAccessExpression,
                                                    IdentifierName(
                                                        "duplicatedValue"
                                                    ),
                                                    IdentifierName(
                                                        cSharpSyntaxNode.ToString()
                                                    )
                                                )
                                            ),
                                            Argument(
                                                MemberAccessExpression(
                                                    SyntaxKind.SimpleMemberAccessExpression,
                                                    IdentifierName(
                                                        "value"
                                                    ),
                                                    IdentifierName(
                                                        cSharpSyntaxNode.ToString()
                                                    )
                                                )
                                            )
                                        }
                                    )
                                )
                            )
                        ),
                        Block(
                            ExpressionStatement(
                                InvocationExpression(
                                    IdentifierName(
                                        $"Unity.Netcode.NetworkVariableSerialization<{type}>.Duplicate"
                                    ),
                                    ArgumentList(
                                        SeparatedList(
                                            new List<ArgumentSyntax>() {
                                                Argument(
                                                        MemberAccessExpression(
                                                            SyntaxKind.SimpleMemberAccessExpression,
                                                            IdentifierName(
                                                                "value"
                                                            ),
                                                            IdentifierName(
                                                                cSharpSyntaxNode.ToString()
                                                            )
                                                        )
                                                    ),
                                                Argument(
                                                        MemberAccessExpression(
                                                            SyntaxKind.SimpleMemberAccessExpression,
                                                            IdentifierName(
                                                                "duplicatedValue"
                                                            ),
                                                            IdentifierName(
                                                                cSharpSyntaxNode.ToString()
                                                            )
                                                        )
                                                    )
                                                    .WithRefKindKeyword(
                                                        Token(
                                                            SyntaxKind.RefKeyword
                                                        )
                                                    )
                                            }
                                        )
                                    )
                                )
                            )
                        )
                    )
                );
            }

            MethodDeclarationSyntax readField = MethodDeclaration(
                    IdentifierName(
                        "void"
                    ),
                    "read"
                )
                .AddModifiers(
                    Token(
                        SyntaxKind.PublicKeyword
                    ),
                    Token(
                        SyntaxKind.StaticKeyword
                    )
                )
                .AddParameterListParameters(
                    Parameter(
                            Identifier(
                                "reader"
                            )
                        )
                        .WithType(
                            IdentifierName(
                                "Unity.Netcode.FastBufferReader"
                            )
                        ),
                    Parameter(
                            Identifier(
                                "value"
                            )
                        )
                        .WithType(
                            IdentifierName(
                                contextTargetNode.Identifier.Text
                            )
                        )
                        .WithModifiers(
                            TokenList(
                                Token(
                                    SyntaxKind.OutKeyword
                                )
                            )
                        )
                )
                .WithBody(
                    Block(
                        readFieldStatement
                    )
                );

            MethodDeclarationSyntax readDeltaField = MethodDeclaration(
                    IdentifierName(
                        "void"
                    ),
                    "readDelta"
                )
                .AddModifiers(
                    Token(
                        SyntaxKind.PublicKeyword
                    ),
                    Token(
                        SyntaxKind.StaticKeyword
                    )
                )
                .AddParameterListParameters(
                    Parameter(
                            Identifier(
                                "readDelta"
                            )
                        )
                        .WithType(
                            IdentifierName(
                                "Unity.Netcode.FastBufferReader"
                            )
                        ),
                    Parameter(
                            Identifier(
                                "value"
                            )
                        )
                        .WithType(
                            IdentifierName(
                                contextTargetNode.Identifier.Text
                            )
                        )
                        .WithModifiers(
                            TokenList(
                                Token(
                                    SyntaxKind.RefKeyword
