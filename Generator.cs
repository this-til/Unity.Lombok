    [Generator]
    public sealed class BufferSerialization : IIncrementalGenerator {
        private static readonly string AttributeName = typeof(ILombokAttribute).FullName!;

        public void Initialize(IncrementalGeneratorInitializationContext context) {
            var sources = context.SyntaxProvider.ForAttributeWithMetadataName(
                AttributeName,
                IsCandidate,
                Transform
            );
            context.AddSources(
                sources
            );
        }

        private bool IsCandidate(
            SyntaxNode node,
            CancellationToken cancellationToken) => node is ClassDeclarationSyntax;

        private GeneratorResult Transform(
            GeneratorAttributeSyntaxContext context,
            CancellationToken cancellationToken) {
            ClassDeclarationSyntax contextTargetNode = (ClassDeclarationSyntax)context.TargetNode;

            SemanticModel semanticModel = context.SemanticModel;

            if (!contextTargetNode.TryValidateType(
                    out var @namespace,
                    out var diagnostic
                )) {
                return new GeneratorResult(
                    diagnostic
                );
            }

            List<CSharpSyntaxNode> fieldList = new List<CSharpSyntaxNode>();

            foreach (var member in contextTargetNode.Members) {
                switch (member) {
                    // 检查成员是否是字段或属性  
                    case FieldDeclarationSyntax fieldDeclaration: {
                        AttributeSyntax? tryGetSpecifiedAttribute = fieldDeclaration.AttributeLists.tryGetSpecifiedAttribute(
                            nameof(BufferFieldAttribute)
                        );
                        if (tryGetSpecifiedAttribute is not null) {
                            foreach (VariableDeclaratorSyntax variableDeclaratorSyntax in fieldDeclaration.Declaration.Variables) {
                                fieldList.Add(
                                    variableDeclaratorSyntax
                                );
                            }
                        }
                        break;
                    }
                    case PropertyDeclarationSyntax propertyDeclaration: {
                        AttributeSyntax? tryGetSpecifiedAttribute = propertyDeclaration.AttributeLists.tryGetSpecifiedAttribute(
                            nameof(BufferFieldAttribute)
                        );
                        if (tryGetSpecifiedAttribute is not null) {
                            fieldList.Add(
                                tryGetSpecifiedAttribute
                            );
                        }
                        break;
                    }
                }
            }

            if (fieldList.Count == 0) {
                return GeneratorResult.Empty;
            }

            List<StatementSyntax> readFieldStatement = new List<StatementSyntax>();
            List<StatementSyntax> readDeltaFieldStatement = new List<StatementSyntax>();

            List<StatementSyntax> writeFieldStatement = new List<StatementSyntax>();
            List<StatementSyntax> writeDeltaFieldStatement = new List<StatementSyntax>();

            List<StatementSyntax> duplicateValueFieldStatement = new List<StatementSyntax>();

            bool isValueType = (semanticModel.GetSymbolInfo(
                                       contextTargetNode
                                   )
                                   .Symbol as ITypeSymbol)?.IsValueType
                               ?? false;

            if (!isValueType) {
                readFieldStatement.Add(
                    LocalDeclarationStatement(
                        VariableDeclaration(
                            ParseTypeName(
                                "bool"
                            ),
                            SeparatedList(
                                new[] {
                                    VariableDeclarator(
                                        "isNull"
                                    )
                                }
                            )
                        )
                    )
                );
                readFieldStatement.Add(
                    ExpressionStatement(
                        InvocationExpression(
                            IdentifierName(
                                "Unity.Netcode.ByteUnpacker.ReadValueBitPacked"
                            ),
                            ArgumentList(
                                SeparatedList(
                                    new[] {
                                        Argument(
                                            IdentifierName(
                                                "reader"
                                            )
                                        ),
                                        Argument(
                                                IdentifierName(
                                                    "isNull"
                                                )
                                            )
                                            .WithRefKindKeyword(
                                                Token(
                                                    SyntaxKind.OutKeyword
                                                )
                                            )
                                    }
                                )
                            )
                        )
                    )
                );

                readFieldStatement.Add(
                    IfStatement(
                        IdentifierName(
                            "isNull"
                        ),
                        Block(
                            ExpressionStatement(
                                AssignmentExpression(
                                    SyntaxKind.SimpleAssignmentExpression,
                                    IdentifierName(
                                        "value"
                                    ),
                                    LiteralExpression(
                                        SyntaxKind.NullLiteralExpression
                                    )
                                )
                            ),
                            ReturnStatement(
                            )
                        )
                    )
                );

                readFieldStatement.Add(
                    ExpressionStatement(
                        AssignmentExpression(
                            SyntaxKind.SimpleAssignmentExpression,
                            IdentifierName(
                                "value"
                            ),
                            ObjectCreationExpression(
                                    ParseTypeName(
                                        contextTargetNode.Identifier.Text
                                    )
                                )
                                .WithArgumentList(
                                    ArgumentList()
                                )
                        )
                    )
                    /*IfStatement(
                        BinaryExpression(
                            SyntaxKind.EqualsExpression,
                            IdentifierName(
                                "value"
                            ),
                            LiteralExpression(
                                SyntaxKind.NullLiteralExpression
                            )
                        ),
                        Block(
                            ExpressionStatement(
