                                                MemberAccessExpression(
                                                    SyntaxKind.SimpleMemberAccessExpression,
                                                    IdentifierName(
                                                        "value"
                                                    ),
                                                    IdentifierName(
                                                        cSharpSyntaxNode.ToString()
                                                    )
                                                )
                                            )
                                            .WithRefKindKeyword(
                                                Token(
                                                    SyntaxKind.RefKeyword
                                                )
                                            )
                                    }
                                )
